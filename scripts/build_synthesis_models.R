#!/usr/bin/env Rscript
# build_synthesis_models.R -- holistic-model synthesis tables for synthesis.Rmd.
#
# Emits the two AUTHORED comparison tables consumed (read-only) by the
# "Candidate holistic models" section of synthesis.Rmd. Content is the curated
# output of the dynamic multi-agent modelling workflow (one unified systems
# model + four adversarially-vetted rivals), distilled to TSV rows here so the
# synthesis report keeps its read-only `rt()` contract (it reads results/*.tsv,
# computes nothing). The wiring schematic is rendered separately by
# scripts/build_synthesis_model_figure.py.
#
# PURE presentation: no cache, no DE, no model fit. Cells are ASCII-safe to
# match the project's results-TSV convention (GSK3b not GSK3-beta, NF-kB, x, ->,
# -|), so read.delim round-trips them; synthesis.Rmd prose carries the Unicode.
#
# Outputs (storage/results/, gitignored + regenerated by this script):
#   synthesis_model_comparison.tsv                 (5 rows: lead + 4 rivals)
#   synthesis_model_discriminating_experiments.tsv (5 rows, cheapest-first)

suppressPackageStartupMessages(library(readr))

RES <- "storage/results"
dir.create(RES, recursive = TRUE, showWarnings = FALSE)

write_tsv_safe <- function(x, path) {
  readr::write_tsv(x, path)
  Sys.chmod(path, mode = "0664")
  try(system2("chown", c("rstudio:rstudio", path)), silent = TRUE)
  cat(sprintf("[synthesis-models] wrote %s (%d rows)\n", path, nrow(x)))
}

